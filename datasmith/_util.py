def repr_truelike_kwargs_no_uid(_obj_, **kwargs):
    kwargs.pop('uid', None)
    return f'{_obj_.__class__.__name__}({", ".join(f"{k}={repr(v)}" for k, v in kwargs.items() if v)})'



